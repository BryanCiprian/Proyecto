package org.itskill.bean;

public class AlmacenBean {

	private Integer idAlmacen;
	private String nomAlmacen;
	private String dirAlmacen;
	private String tipoProd;
	public Integer getIdAlmacen() {
		return idAlmacen;
	}
	public void setIdAlmacen(Integer idAlmacen) {
		this.idAlmacen = idAlmacen;
	}
	public String getNomAlmacen() {
		return nomAlmacen;
	}
	public void setNomAlmacen(String nomAlmacen) {
		this.nomAlmacen = nomAlmacen;
	}
	public String getDirAlmacen() {
		return dirAlmacen;
	}
	public void setDirAlmacen(String dirAlmacen) {
		this.dirAlmacen = dirAlmacen;
	}
	public String getTipoProd() {
		return tipoProd;
	}
	public void setTipoProd(String tipoProd) {
		this.tipoProd = tipoProd;
	}
	
	
}
