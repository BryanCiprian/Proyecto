package org.itskill.bean;

public class ZonaBean {
   
	private Integer idZona;
	private DistritoBean distritoBean;
	private String desZona;
	public Integer getIdZona() {
		return idZona;
	}
	public void setIdZona(Integer idZona) {
		this.idZona = idZona;
	}
	public DistritoBean getDistritoBean() {
		return distritoBean;
	}
	public void setDistritoBean(DistritoBean distritoBean) {
		this.distritoBean = distritoBean;
	}
	public String getDesZona() {
		return desZona;
	}
	public void setDesZona(String desZona) {
		this.desZona = desZona;
	}
	

}
