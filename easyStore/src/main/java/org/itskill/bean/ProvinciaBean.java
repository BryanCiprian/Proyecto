package org.itskill.bean;

public class ProvinciaBean {

	private Integer idProvincia;
	private DepartamentoBean departamentoBean;
	private Integer codProvincia;
	private String desProvincia;
	public Integer getIdProvincia() {
		return idProvincia;
	}
	public void setIdProvincia(Integer idProvincia) {
		this.idProvincia = idProvincia;
	}
	public DepartamentoBean getDepartamentoBean() {
		return departamentoBean;
	}
	public void setDepartamentoBean(DepartamentoBean departamentoBean) {
		this.departamentoBean = departamentoBean;
	}
	public Integer getCodProvincia() {
		return codProvincia;
	}
	public void setCodProvincia(Integer codProvincia) {
		this.codProvincia = codProvincia;
	}
	public String getDesProvincia() {
		return desProvincia;
	}
	public void setDesProvincia(String desProvincia) {
		this.desProvincia = desProvincia;
	}
	
	
}
