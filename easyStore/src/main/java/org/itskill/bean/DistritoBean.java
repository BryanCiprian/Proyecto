package org.itskill.bean;

public class DistritoBean {

	private Integer idDistrito;
	private ProvinciaBean provinciaBean;
	private Integer codDistrito;
	private String desDistrito;
	public Integer getIdDistrito() {
		return idDistrito;
	}
	public void setIdDistrito(Integer idDistrito) {
		this.idDistrito = idDistrito;
	}
	public ProvinciaBean getProvinciaBean() {
		return provinciaBean;
	}
	public void setProvinciaBean(ProvinciaBean provinciaBean) {
		this.provinciaBean = provinciaBean;
	}
	public Integer getCodDistrito() {
		return codDistrito;
	}
	public void setCodDistrito(Integer codDistrito) {
		this.codDistrito = codDistrito;
	}
	public String getDesDistrito() {
		return desDistrito;
	}
	public void setDesDistrito(String desDistrito) {
		this.desDistrito = desDistrito;
	}
	
	
}
